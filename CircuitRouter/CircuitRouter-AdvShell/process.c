#include "Process.h"
#include "../lib/timer.h"
#include <stdlib.h>
#include <stdio.h>

/*******************************************************************************
 * createProcess
 *******************************************************************************
*/
Process * createProcess(int pid) {
    Process * p = malloc(sizeof(struct process));
    p->pid = pid;
    p->status = -1;
    return p;
}

/*******************************************************************************
 * freeProcess
 *******************************************************************************
*/
void freeProcess(Process * p) {
    free(p);
}

/*******************************************************************************
 * processTime
 *******************************************************************************
*/
float processTime(Process * p) {
    return TIMER_DIFF_SECONDS(p->start, p->finish);
}

/*******************************************************************************
 * status
 *******************************************************************************
*/
int status(Process * p) {
    return p->status;
}

/*******************************************************************************
 * getPid
 *******************************************************************************
*/
int getPid(Process * p) {
    return p->pid;
}

/*******************************************************************************
 * printProcess
 *******************************************************************************
*/
void printProcess(Process * p) {
    printf("CHILD EXITED (PID=%d; return %s; %.0f)\n", getPid(p), 
                                    (status(p) == 0 ? "OK" : "NOK"), processTime(p));
}